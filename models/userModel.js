const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    min: 3,
    max: 20,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    max: 50,
  },
  password: {
    type: String,
    required: true,
    min: 8,
  },
  isAvatarImageSet: {
    type: Boolean,
    default: false,
  },
  avatarImage: {
    type: String,
    default: "",
  },
  userFcm: {
    type: String,
    default: "",
  },
  lastSeen: {
    type: Date,
    default: Date.now, // Automatically set to the current timestamp
  },
});

module.exports = mongoose.model("Users", userSchema);
